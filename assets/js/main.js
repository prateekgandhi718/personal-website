/*==================== SERVICES MODAL ====================*/

const modalViews = document.querySelectorAll('.services__modal'),
    modalBtns = document.querySelectorAll('.services__button'),
    modalCloses = document.querySelectorAll('.services__modal-close')

let modal = function(modalClick){ //modalClick is just a argument, we are passing i in it.
    modalViews[modalClick].classList.add('active-modal')
}

modalBtns.forEach((modalBtn, i) =>{
    modalBtn.addEventListener('click', () =>{
        modal(i)
    })
})

modalCloses.forEach((modalClose) =>{
    modalClose.addEventListener('click', () =>{
        modalViews.forEach((modalView) =>{
            modalView.classList.remove('active-modal')
        })
    })
})

/*
Explaination of above code:
modal is function which adds the class active-modal to the modalView which is passed into the function.

Iterating over each service button which are modalBtns and a click event listener is added which triggers the modal function for the particular modalBtn which is clicked.

modalCloses has all the cross signs. Iterating over them and adding a click evernt listener and then iterating over modalViews which removes the active-modal from the modalView whose cross icon is clicked!
*/

